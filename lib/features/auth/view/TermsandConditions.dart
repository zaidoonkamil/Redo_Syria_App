القسم الأول: شروط وأحكام الراكب

هذا القسم يوضح جميع الحقوق والالتزامات والمسؤوليات الخاصة بالراكب (العميل) عند استخدام تطبيق ريدو.

أولاً: إنشاء الحساب والأهلية
1. الأهلية القانونية
• يجب ألا يقل عمر المستخدم عن 16 عاماً لإنشاء حساب مستقل في التطبيق.
• يمكن للقاصرين (دون 16 عاماً) استخدام الخدمة فقط تحت إشراف وتواجد شخص بالغ.

2. دقة المعلومات الشخصية
• يلتزم الراكب بتقديم معلومات دقيقة عند التسجيل، تشمل الاسم الكامل ورقم الهاتف الجوال العراقي الفعال.
• يجب تحديث هذه المعلومات باستمرار لضمان صحتها.
• يلتزم الراكب بتوثيق حسابه برقم هاتف عراقي مسجل باسمه لدى شركات الاتصال.

3. سرية بيانات الحساب
• الراكب هو المسؤول الوحيد عن الحفاظ على سرية بيانات الدخول الخاصة به.
• يتحمل الراكب مسؤولية أي نشاط يتم من خلال حسابه سواء أجراه بنفسه أم لا.

ثانياً: آلية طلب الخدمة وتأكيدها
1. طلب الرحلة
• يقوم الراكب بتحديد موقع الانطلاق والوجهة عند طلب الرحلة عبر التطبيق.
• يُعدّ تأكيد الطلب من قِبل الراكب موافقة صريحة على السعر التقديري الظاهر في التطبيق.

2. الالتزام بنقطة الانطلاق
• يلتزم الراكب بالتواجد في نقطة الانطلاق المحددة في الوقت المناسب.
• عدم الالتزام بالوقت قد يؤدي إلى إلغاء الرحلة أو فرض رسوم انتظار.

ثالثاً: قواعد السلوك داخل المركبة
1. الاحترام والنظافة
• التعامل باحترام تام مع السائق في جميع الأوقات.
• الحفاظ على نظافة المركبة وعدم إلحاق أي ضرر بها.
• يُحظر التدخين داخل السيارة إلا بموافقة صريحة من السائق.

2. عدد الركاب
• يلتزم الراكب بالعدد المحدد للركاب وفقاً لنوع المركبة المختارة.
• يجب الامتثال لأحكام قانون المرور العراقي المتعلقة بعدد الركاب.

رابعاً: الأنشطة المحظورة على الراكب
1. المحظورات القانونية والأمنية
• يُمنع منعاً باتاً حيازة أو نقل أي مواد يجرمها القانون العراقي (أسلحة، ذخائر، مواد مخدرة، مشروبات كحولية، أو مواد مسروقة).
• يُحظر نقل المواد القابلة للاشتعال أو الكيميائية الخطرة أو أي أشياء تهدد سلامة الأطراف أو المركبة.
• يُحظر انتحال شخصية موظف في شركة ريدو أو استخدام حساب شخص آخر.

2. محظورات الاستخدام
• يُمنع استخدام التطبيق لنقل البضائع أو الطرود دون وجود راكب؛ المنصة مخصصة لنقل الأفراد فقط.
• يُحظر إجراء طلبات رحلات وهمية بهدف إرباك النظام أو الإضرار بالسائقين.
• يُمنع الاتفاق على رحلات خارج نظام التطبيق للتهرب من الرسوم؛ وفي هذه الحالة لا تتحمل المنصة أي مسؤولية.

3. السلوك والآداب العامة
• يُمنع منعاً باتاً أي شكل من أشكال التحرش (لفظياً أو فعلياً) أو استخدام لغة بذيئة.
• يُمنع التمييز بناءً على الدين أو الطائفة أو العرق أو الجنس.
• يُحظر تصوير السائق أو تسجيل صوته دون إذن صريح منه.
• يُمنع محاولة الحصول على البيانات الشخصية للسائق للتواصل معه خارج نطاق الخدمة.
• يُمنع تعمد إتلاف أو توسيخ مقاعد السيارة أو ممتلكات السائق.
• يُمنع الخوض في نقاشات سياسية أو طائفية حادة قد تسبب نزاعات داخل المركبة.

4. المحظورات التقنية
• يُحظر استخدام برامج GPS وهمية (Fake GPS) للتلاعب بالموقع الجغرافي.
• يُمنع استخدام أدوات برمجية لسحب البيانات من التطبيق.
• يُحظر إنشاء أكثر من حساب لنفس الغرض بهدف التلاعب بالعروض الترويجية.

خامساً: الرسوم والدفعات الخاصة بالراكب
1. تعرفة الرحلة
• يتم احتساب تكلفة الرحلة بناءً على معادلة تشمل: أجرة الأساس + المسافة المقطوعة + وقت الرحلة.
• قد تتأثر الأسعار في أوقات الذروة؛ ويظهر السعر التقديري للراكب في التطبيق قبل تأكيد الطلب.
• يلتزم الراكب بدفع القيمة الظاهرة في التطبيق عند نهاية الرحلة، بالإضافة إلى أي رسوم إضافية (مثل رسوم بوابات المطارات أو الطرق ذات الرسوم الخاصة).

2. طرق الدفع المتاحة
• الدفع النقدي (Cash): يتم تسليم المبلغ مباشرة للسائق عند نهاية الرحلة.
• المحافظ الإلكترونية: مثل ZainCash وAsiaPay أو أي محفظة أخرى مدعومة داخل التطبيق.
• الرصيد داخل التطبيق: يمكن للراكب شحن محفظته الخاصة في تطبيق ريدو واستخدامها لدفع ثمن الرحلات.

3. رسوم الإلغاء
• يحق للمنصة فرض رسوم إلغاء على الراكب في حال تم الإلغاء بعد مرور وقت معين من قبول السائق للطلب.
• تفرض رسوم إلغاء أيضاً إذا وصل السائق لنقطة الانطلاق ولم يخرج الراكب.
• يتم خصم هذه الرسوم من رصيد المحفظة أو إضافتها كدين على الرحلة القادمة.

4. رسوم الانتظار
• تمنح المنصة الراكب فترة سماح مجانية (مثلاً 5 دقائق) عند وصول السائق.
• في حال تجاوز هذه المدة، يحق للسائق بدء احتساب رسوم انتظار تضاف إلى القيمة الإجمالية للرحلة.

5. الفواتير والاعتراض
• عند نهاية كل رحلة يتم إصدار ملخص إلكتروني (فاتورة) يوضح تفاصيل الأجرة عبر التطبيق.
• في حال وجود اعتراض على قيمة الرحلة، يجب تقديم شكوى عبر قسم الدعم الفني خلال 24 ساعة من موعد الرحلة.

سادساً: الاتصالات والتواصل الخاص بالراكب
1. التواصل مع السائق
• يوفر التطبيق وسيلة اتصال لتنسيق نقطة الالتقاء أو تفاصيل الرحلة فقط.
• يجب أن تظل جميع الاتصالات ضمن إطار الاحترام المتبادل والمهنية.
• يُمنع استخدام وسائل التواصل لأغراض شخصية أو التحرش أو الإساءة اللفظية.
• بمجرد اكتمال الرحلة، تنتهي صلاحية التواصل. يُحظر الاحتفاظ ببيانات اتصال السائق أو مراسلته خارج نطاق المنصة.

2. الرسائل الواردة من المنصة
• بإنشاء الحساب، يوافق الراكب على تلقي رسائل تشغيلية مثل رموز التحقق (OTP) وإشعارات تأكيد الرحلة والفواتير.
• يتلقى الراكب إشعارات إدارية تشمل تحديثات الشروط والأحكام وتنبيهات الأمان.
• يمكن إلغاء الاشتراك في الرسائل الترويجية عبر إعدادات التطبيق.

3. تسجيل المكالمات
• قد يتم تسجيل أو مراقبة الاتصالات التي تتم عبر التطبيق لضمان جودة الخدمة وسلامة الأطراف.
• تُستخدم هذه التسجيلات في حالات التحقيق في الشكاوى أو التدريب أو الامتثال للجهات القانونية.

سابعاً: الخصوصية وحماية البيانات
1. حق الوصول إلى البيانات
• يحق للراكب الاطلاع على بياناته الشخصية المحتفظ بها في المنصة، وتشمل: معلومات الملف الشخصي، سجل الرحلات، التقييمات والملاحظات، والسجل المالي.

2. حق تصحيح البيانات
• يمكن للراكب في أي وقت تعديل بياناته غير الدقيقة عبر إعدادات التطبيق.
• يمكن طلب تصحيح البيانات التي لا يمكن الوصول إليها عبر الدعم الفني.

3. حق حذف الحساب
• يحق للراكب طلب إغلاق حسابه وحذف بياناته الشخصية في أي وقت.
• الحذف الذاتي عبر: الإعدادات - الحساب - حذف الحساب.
• أو عبر إرسال طلب رسمي من البريد الإلكتروني أو رقم الهاتف المسجل إلى بريد الدعم الفني.

4. استثناءات الاحتفاظ بالبيانات
• قد تحتفظ المنصة ببعض البيانات للامتثال القانوني والقضائي وفق ما يحدده القانون العراقي.
• لن يتم حذف البيانات في حال وجود شكوى معلقة أو مبالغ مالية غير مدفوعة إلا بعد تسوية النزاع.
• بعد معالجة طلب الحذف، لن يكون الراكب قادراً على استعادة سجلات الرحلات أو الأرصدة المرتبطة بالحساب.

ثامناً: حدود مسؤولية الراكب والمنصة
1. مسؤولية الراكب
• باستخدامه للتطبيق، يقر الراكب بقبول شروط الاتفاقية كاملةً.
• يتحمل الراكب مسؤولية قبوله للرحلة مع السائق باعتبار ذلك قراراً شخصياً.
• يلتزم الراكب بتعويض المنصة عن أي مطالبات تنشأ عن سوء استخدامه للتطبيق أو انتهاكه للشروط.

2. مسؤولية المنصة تجاه الراكب
• تقدم المنصة خدماتها "كما هي"؛ ولا تضمن توفر السائقين في كل الأوقات أو خلو الخدمة من الانقطاعات التقنية.
• لا تتحمل المنصة مسؤولية الحوادث المرورية أو الإصابات أو تلف الممتلكات الشخصية أثناء الرحلة؛ هذه تقع على عاتق السائق.
• توفر المنصة قنوات تواصل للمساعدة في استعادة المفقودات دون أن تتحمل مسؤولية قانونية عنها.
• تتعاون المنصة مع السلطات الأمنية العراقية بتزويدهم البيانات اللازمة عند الطلب الرسمي.

تاسعاً: الامتثال القانوني الخاص بالراكب
• يلتزم الراكب بقانون المرور العراقي في كل ما يتعلق بالسلامة والسلوك داخل المركبة.
• يلتزم بقانون جرائم المعلوماتية العراقي؛ يُحظر استخدام التطبيق لإزعاج الآخرين أو اختراق الخصوصية.
• في حال أي نزاع قانوني، يكون القضاء العراقي هو المرجع الوحيد لحل النزاعات.
• جميع السجلات الرقمية وبيانات GPS والرسائل المتبادلة عبر التطبيق تتمتع بحجية قانونية كاملة أمام المحاكم العراقية.
• تعتبر ضغطة الزر داخل التطبيق (مثل "تأكيد الطلب") بمثابة توقيع إلكتروني ملزم.

القسم الثاني: شروط وأحكام السائق (الكابتن)

هذا القسم يوضح جميع الحقوق والالتزامات والمسؤوليات الخاصة بالسائق (الكابتن / مزود الخدمة) عند التسجيل والعمل في تطبيق ريدو.

أولاً: الوضع القانوني للسائق
1. استقلالية السائق
• جميع السائقين المسجلين في التطبيق هم أطراف خارجية مستقلة (Independent Contractors).
• السائق ليس موظفاً ولا وكيلاً ولا ممثلاً لتطبيق ريدو.
• عند قبول السائق لطلب الرحلة، ينشأ عقد نقل مباشر بينه وبين الراكب دون تدخل المنصة.

2. طبيعة العلاقة التعاقدية
• دور ريدو ينتهي بمجرد ربط الراكب بالسائق المناسب عبر التطبيق.
• السائق هو الطرف الملزم بنقل الراكب من نقطة الانطلاق إلى نقطة الوصول المحددة مقابل الرسوم المتفق عليها.

ثانياً: التراخيص والوثائق المطلوبة من السائق
1. إجازة السوق
• يجب أن يحمل السائق إجازة سوق عراقية نافذة وصالحة.
• يجب أن تكون فئة الإجازة متناسبة مع نوع المركبة المستخدمة.

2. أوراق المركبة
• يجب أن تكون السيارة مسجلة رسمياً لدى مديرية المرور العامة بسنوية نافذة.
• يجب أن تحمل المركبة لوحات تسجيل أصولية ومعتمدة.

3. التراخيص الأمنية والهوية
• يقر السائق بصحة كافة مستمسكاته الثبوتية المرفوعة للمنصة (هوية الأحوال المدنية أو البطاقة الوطنية).
• يتحمل السائق المسؤولية القانونية الكاملة عن أي تلاعب في هذه البيانات.
• يُعتبر تقديم أي وثيقة مزورة جريمة يحاسب عليها القانون العراقي وتؤدي للحظر الفوري وتقديم البيانات للسلطات.

ثالثاً: مسؤوليات السائق الكاملة
1. المسؤوليات التشغيلية
• صيانة المركبة وجهوزيتها الفنية والحفاظ عليها في حالة جيدة.
• الالتزام الكامل بقواعد المرور والقوانين المحلية للنقل في العراق.
• ضمان سلامة الراكب طوال فترة الرحلة.
• الالتزام بمعايير الجودة المطلوبة من المنصة وعدم الإخلال بها.

2. سلوك السائق
• التعامل باحترام تام مع الركاب في جميع الأوقات.
• يحق للسائق رفض أو إلغاء الرحلة إذا تبين أن الغرض منها نقل بضائع دون راكب.
• يحق للسائق إلغاء الرحلة إذا لاحظ حيازة الراكب لمواد غير قانونية.
• الوصول لنقطة الانطلاق في الوقت المناسب وإخطار الراكب عند الوصول.

3. المسؤولية عن الحوادث
• السائق هو المسؤول الوحيد عن أي حوادث مرورية أو إصابات ناتجة عن الرحلة.
• تخضع الحوادث لتأمين المركبة والتشريعات المحلية.
• لا تتحمل منصة ريدو مسؤولية الحوادث المرورية أو سوء تصرف السائق.

رابعاً: الأنشطة المحظورة على السائق
1. المحظورات القانونية
• يُمنع منعاً باتاً نقل أي مواد يجرمها القانون العراقي (أسلحة، مواد مخدرة، مشروبات كحولية، مواد مسروقة).
• يُحظر نقل المواد القابلة للاشتعال أو الكيميائية الخطرة.
• يُحظر انتحال الشخصية أو استخدام وثائق مزورة أو بيانات وهمية.

2. محظورات الخدمة
• يُمنع الاتفاق على رحلات خارج نظام التطبيق للتهرب من رسوم المنصة.
• يُحظر إجراء تصرفات تُعطّل عمل المنصة تقنياً أو تضر بسمعتها.

3. السلوك والآداب العامة
• يُمنع أي شكل من أشكال التحرش (لفظياً أو فعلياً) تجاه الركاب.
• يُمنع التمييز بناءً على الدين أو الطائفة أو العرق أو الجنس.
• يُحظر تصوير الراكب أو تسجيل صوته دون إذن صريح منه.
• يُمنع الاحتفاظ ببيانات اتصال الراكب أو التواصل معه خارج نطاق المنصة بعد انتهاء الرحلة.
• يُمنع الخوض في نقاشات سياسية أو طائفية مع الركاب.

4. المحظورات التقنية
• يُحظر استخدام برامج GPS وهمية للتلاعب بالموقع الجغرافي.
• يُمنع محاولة اختراق التطبيق أو التلاعب ببياناته.
• يُحظر إنشاء أكثر من حساب للتلاعب بالعروض الترويجية أو رسوم المنصة.

خامساً: التوثيق والمراقبة
1. كاميرات المراقبة
• يحق للسائق (بموافقة المنصة) استخدام وسائل توثيق لضمان الجودة وفق القوانين العراقية الخاصة بالخصوصية.
• في حال وجود كاميرات مراقبة داخل المركبة، يجب وضع ملصق واضح يُعلم الراكب بذلك.
• تُستخدم التسجيلات فقط لغرض التوثيق الأمني والقانوني عند الضرورة.

2. سجلات الرحلات
• تقوم المنصة بتوثيق وحفظ سجلات الرحلات (المسار، الوقت، الأجرة، بيانات الأطراف) في قواعد بيانات مؤمنة.
• تُعامل هذه البيانات بخصوصية تامة، ولا يتم الإفصاح عنها إلا في حالات النزاع القانوني أو بناءً على أمر قضائي.

سادساً: الاتصالات الخاصة بالسائق
1. التواصل مع الراكب
• يوفر التطبيق وسيلة اتصال للتنسيق بشأن نقطة الالتقاء أو تفاصيل الرحلة فقط.
• يجب أن تكون جميع الاتصالات ضمن إطار الاحترام المتبادل والمهنية.
• يُمنع استخدام وسائل التواصل لأغراض شخصية أو التحرش أو الإساءة اللفظية.
• بمجرد اكتمال الرحلة، تنتهي صلاحية التواصل مع الراكب ويُحظر مراسلته خارج المنصة.

سابعاً: الحقوق المالية للسائق
1. رسوم الانتظار
• تمنح المنصة الراكب فترة سماح مجانية (مثلاً 5 دقائق) عند وصول السائق لنقطة الانطلاق.
• بعد انتهاء فترة السماح، يحق للسائق بدء احتساب رسوم انتظار تضاف إلى القيمة الإجمالية للرحلة.

2. رسوم الإلغاء
• يحق للسائق تلقي رسوم إلغاء من المنصة في حال قام الراكب بالإلغاء بعد مرور وقت معين من قبوله للطلب.
• تفرض رسوم إلغاء أيضاً إذا وصل السائق لنقطة الانطلاق ولم يخرج الراكب في الوقت المحدد.

ثامناً: حقوق السائق على المنصة
1. التمكين التقني والتدريب
• تلتزم ريدو بتوفير التدريب اللازم والأدوات التقنية للسائقين لمساعدتهم على إدارة دخلهم.
• توفر المنصة للسائقين فرص عمل مرنة تساهم في تطوير مهاراتهم في التعامل مع التكنولوجيا.

2. حق الوصول إلى البيانات
• يحق للسائق الاطلاع على بياناته الشخصية المحتفظ بها في المنصة (معلومات الملف الشخصي، سجل الرحلات، التقييمات، السجل المالي).
• يمكن للسائق تعديل بياناته غير الدقيقة عبر إعدادات التطبيق أو التواصل مع الدعم الفني.

3. حق طلب حذف الحساب
• يحق للسائق طلب إغلاق حسابه وحذف بياناته الشخصية عبر إعدادات التطبيق أو طلب رسمي لبريد الدعم الفني.
• مع مراعاة أن بعض البيانات قد تُحتفظ بها للامتثال القانوني أو في حال وجود نزاعات أو مبالغ غير مسددة.

تاسعاً: الامتثال القانوني الخاص بالسائق
• يلتزم السائق بقانون المرور العراقي في كل ما يتعلق بالسلامة والسرعة والالتزام بقواعد السير.
• يلتزم بقانون جرائم المعلوماتية العراقي؛ يُحظر استخدام التطبيق لإزعاج الآخرين أو اختراق الخصوصية.
• في حال أي نزاع قانوني، يكون القضاء العراقي هو المرجع الوحيد، والمحاكم في بغداد هي المختصة مكانياً.
• جميع السجلات الرقمية وبيانات GPS والرسائل المتبادلة عبر التطبيق تتمتع بحجية قانونية كاملة أمام المحاكم العراقية.
• تعتبر ضغطة الزر داخل التطبيق (مثل "قبول الرحلة" أو "إنهاء الرحلة") بمثابة توقيع إلكتروني ملزم.
• يقر السائق بأن استخدامه لوسائل الاتصال عبر التطبيق يخضع للقوانين والأنظمة الصادرة عن هيئة الإعلام والاتصالات العراقية.

عاشراً: حظر حساب السائق وإيقافه
• يحق للمنصة حظر حساب السائق فوراً في حال تقديم وثائق مزورة أو مخالفة الشروط.
• يتعرض السائق للحظر الفوري عند القيام بأي نشاط مخالف للقوانين العراقية أو معايير المنصة.
• في حالات الحظر، تحتفظ المنصة بحق تقديم بيانات السائق للسلطات الأمنية المختصة.
• يحق للمنصة حظر السائقين غير الملتزمين بمعايير الجودة والمهنية المطلوبة.

ملاحظة ختامية مشتركة
تسري هذه الشروط والأحكام على جميع مستخدمي منصة ريدو (ركاباً وسائقين). بمجرد تحميل التطبيق أو إنشاء حساب أو استخدام أي من الخدمات، فإن المستخدم يُقر بأنه قد قرأ وفهم ووافق على الالتزام بكافة البنود الواردة في هذه الاتفاقية.
تحتفظ منصة ريدو بالحق في تحديث هذه الشروط والأحكام من وقت لآخر، وسيتم الإخطار بأي تغييرات جوهرية عبر التطبيق أو البريد الإلكتروني.

شروط وأحكام الراكب
جميع الحقوق والالتزامات الخاصة بالراكب عند استخدام تطبيق ريدو


هذا المستند يوضح بشكل تفصيلي جميع الحقوق والالتزامات والمسؤوليات الخاصة بالراكب عند استخدام تطبيق ريدو. يرجى قراءة هذه الشروط بعناية قبل استخدام الخدمة، إذ يُعدّ استخدامك للتطبيق موافقة ضمنية على جميع البنود الواردة أدناه.

أولاً: إنشاء الحساب والأهلية
الأهلية القانونية
يجب ألا يقل عمر المستخدم عن 16 عاماً لإنشاء حساب مستقل في التطبيق.
يمكن للقاصرين دون سن 16 استخدام الخدمة فقط تحت إشراف وتواجد شخص بالغ.
دقة المعلومات الشخصية
يلتزم الراكب بتقديم معلومات دقيقة عند التسجيل، تشمل الاسم الكامل ورقم الهاتف الجوال العراقي الفعال.
يجب تحديث هذه المعلومات باستمرار لضمان صحتها وحداثتها.
يلتزم الراكب بتوثيق حسابه برقم هاتف عراقي مسجل باسمه لدى شركات الاتصال المعتمدة.
سرية بيانات الحساب
الراكب هو المسؤول الوحيد عن الحفاظ على سرية بيانات الدخول الخاصة به.
يتحمل الراكب مسؤولية أي نشاط يتم من خلال حسابه سواء أجراه بنفسه أم لا.

ثانياً: آلية طلب الخدمة وتأكيدها
طلب الرحلة
يقوم الراكب بتحديد موقع الانطلاق والوجهة عند طلب الرحلة عبر التطبيق.
يُعدّ تأكيد الطلب من قِبل الراكب موافقة صريحة على السعر التقديري الظاهر في التطبيق.
الالتزام بنقطة الانطلاق
يلتزم الراكب بالتواجد في نقطة الانطلاق المحددة في الوقت المناسب.
عدم الالتزام بالوقت قد يؤدي إلى إلغاء الرحلة أو فرض رسوم انتظار على الراكب.

ثالثاً: قواعد السلوك داخل المركبة
الاحترام والنظافة
التعامل باحترام تام مع السائق في جميع الأوقات.
الحفاظ على نظافة المركبة وعدم إلحاق أي ضرر بها أو بمحتوياتها.
يُحظر التدخين داخل السيارة إلا بموافقة صريحة من السائق.
عدد الركاب
يلتزم الراكب بالعدد المحدد للركاب وفقاً لنوع المركبة المختارة.
يجب الامتثال لأحكام قانون المرور العراقي المتعلقة بعدد الركاب المسموح به.

رابعاً: الأنشطة المحظورة على الراكب
المحظورات القانونية والأمنية
يُمنع منعاً باتاً حيازة أو نقل أي مواد يجرمها القانون العراقي، كالأسلحة والذخائر والمواد المخدرة والمشروبات الكحولية والمواد المسروقة.
يُحظر نقل المواد القابلة للاشتعال أو الكيميائية الخطرة أو أي أشياء تهدد سلامة الأطراف أو المركبة.
يُحظر انتحال شخصية موظف في شركة ريدو أو إنشاء حساب ببيانات وهمية أو استخدام حساب شخص آخر.
محظورات الاستخدام
يُمنع استخدام التطبيق لنقل البضائع أو الطرود دون وجود راكب، إذ المنصة مخصصة لنقل الأفراد فقط.
يُحظر إجراء طلبات رحلات وهمية بهدف إرباك النظام أو الإضرار بالسائقين.
يُمنع الاتفاق على رحلات خارج نظام التطبيق للتهرب من الرسوم، وفي هذه الحالة لا تتحمل المنصة أي مسؤولية عن أي حادث أو نزاع.
السلوك والآداب العامة
يُمنع منعاً باتاً أي شكل من أشكال التحرش سواء لفظياً أو فعلياً، أو استخدام لغة بذيئة.
يُمنع التمييز بناءً على الدين أو الطائفة أو العرق أو الجنس.
يُحظر تصوير السائق أو تسجيل صوته دون إذن صريح منه.
يُمنع محاولة الحصول على البيانات الشخصية للسائق للتواصل معه خارج نطاق الخدمة.
يُمنع تعمد إتلاف أو توسيخ مقاعد السيارة أو ممتلكات السائق.
يُنصح بتجنب الخوض في نقاشات سياسية أو طائفية حادة قد تسبب نزاعات داخل المركبة.
المحظورات التقنية
يُحظر استخدام برامج GPS وهمية للتلاعب بالموقع الجغرافي.
يُمنع استخدام أدوات برمجية لسحب البيانات من التطبيق أو محاولة اختراقه.
يُحظر إنشاء أكثر من حساب لنفس الغرض بهدف التلاعب بالعروض الترويجية.

خامساً: الرسوم والدفعات
تعرفة الرحلة
يتم احتساب تكلفة الرحلة بناءً على معادلة تشمل: أجرة الأساس، المسافة المقطوعة، ووقت الرحلة.
قد تتأثر الأسعار في أوقات الذروة كالازدحامات الخانقة أو الظروف الجوية القاسية، ويظهر السعر التقديري للراكب في التطبيق قبل تأكيد الطلب.
يلتزم الراكب بدفع القيمة الظاهرة في التطبيق عند نهاية الرحلة، بالإضافة إلى أي رسوم إضافية كرسوم بوابات المطارات أو الطرق ذات الرسوم الخاصة.
طرق الدفع المتاحة
الدفع النقدي: يتم تسليم المبلغ مباشرة للسائق عند نهاية الرحلة.
المحافظ الإلكترونية: مثل ZainCash وAsiaPay أو أي محفظة أخرى مدعومة داخل التطبيق.
الرصيد داخل التطبيق: يمكن للراكب شحن محفظته الخاصة في التطبيق واستخدامها لدفع ثمن الرحلات.
رسوم الإلغاء
يحق للمنصة فرض رسوم إلغاء على الراكب في حال تم الإلغاء بعد مرور وقت معين من قبول السائق للطلب.
تفرض رسوم إلغاء أيضاً إذا وصل السائق لنقطة الانطلاق ولم يخرج الراكب في الوقت المحدد.
يتم خصم هذه الرسوم من رصيد المحفظة أو إضافتها كدين على الرحلة القادمة.
رسوم الانتظار
تمنح المنصة الراكب فترة سماح مجانية عند وصول السائق لنقطة الانطلاق (مثلاً 5 دقائق).
في حال تجاوز هذه المدة، يحق للسائق بدء احتساب رسوم انتظار تضاف إلى القيمة الإجمالية للرحلة.
الفواتير والاعتراض
عند نهاية كل رحلة يتم إصدار ملخص إلكتروني يوضح تفاصيل الأجرة عبر التطبيق.
في حال وجود اعتراض على قيمة الرحلة، يجب تقديم شكوى عبر قسم الدعم الفني في التطبيق خلال 24 ساعة من موعد الرحلة.

سادساً: الاتصالات والتواصل
التواصل مع السائق
يوفر التطبيق وسيلة اتصال لتنسيق نقطة الالتقاء أو تفاصيل الرحلة فقط، ولا يجوز استخدامها لأغراض أخرى.
يجب أن تظل جميع الاتصالات ضمن إطار الاحترام المتبادل والمهنية.
يُمنع استخدام وسائل التواصل للتحرش أو الإساءة اللفظية أو لأغراض شخصية.
بمجرد اكتمال الرحلة تنتهي صلاحية التواصل، ويُحظر الاحتفاظ ببيانات اتصال السائق أو مراسلته خارج نطاق المنصة.
الرسائل الواردة من المنصة
بإنشاء الحساب، يوافق الراكب على تلقي رسائل تشغيلية مثل رموز التحقق وإشعارات تأكيد الرحلة والفواتير.
يتلقى الراكب إشعارات إدارية تشمل تحديثات الشروط وتنبيهات الأمان.
يمكن إلغاء الاشتراك في الرسائل الترويجية عبر إعدادات التطبيق في أي وقت.
تسجيل المكالمات
قد يتم تسجيل أو مراقبة الاتصالات التي تتم عبر التطبيق لضمان جودة الخدمة وسلامة الأطراف.
تُستخدم هذه التسجيلات فقط في حالات التحقيق في الشكاوى أو التدريب أو الامتثال للجهات القانونية العراقية.

سابعاً: الخصوصية وحماية البيانات
حق الوصول إلى البيانات
يحق للراكب الاطلاع على بياناته الشخصية المحتفظ بها في المنصة، وتشمل: معلومات الملف الشخصي، سجل الرحلات السابقة، التقييمات والملاحظات، والسجل المالي.
حق تصحيح البيانات
يمكن للراكب في أي وقت تعديل بياناته غير الدقيقة عبر إعدادات التطبيق.
يمكن طلب تصحيح البيانات التي لا يمكن الوصول إليها من خلال التواصل مع الدعم الفني.
حق حذف الحساب والبيانات
يحق للراكب طلب إغلاق حسابه وحذف بياناته الشخصية في أي وقت عبر خيار الحذف الذاتي في الإعدادات.
يمكن أيضاً تقديم طلب رسمي عبر البريد الإلكتروني أو رقم الهاتف المسجل في الحساب إلى بريد الدعم الفني.
استثناءات الاحتفاظ بالبيانات
قد تحتفظ المنصة ببعض البيانات للامتثال القانوني والقضائي وفق ما يحدده القانون العراقي.
لن يتم حذف البيانات في حال وجود شكوى معلقة أو مبالغ مالية غير مدفوعة إلا بعد تسوية النزاع بالكامل.
بعد معالجة طلب الحذف لن يكون الراكب قادراً على استعادة سجلات الرحلات أو الأرصدة أو الخصومات المرتبطة بالحساب المحذوف.

ثامناً: حدود المسؤولية
مسؤولية الراكب
باستخدامه للتطبيق، يقر الراكب بقبول شروط الاتفاقية كاملة وبمسؤوليته الشخصية عن قراراته داخل الخدمة.
يلتزم الراكب بتعويض المنصة عن أي مطالبات أو خسائر تنشأ عن سوء استخدامه للتطبيق أو مخالفته للشروط.
يلتزم الراكب بتعويض المنصة في حال انتهاكه لحقوق أي طرف ثالث أو مخالفته للقوانين العراقية النافذة.
مسؤولية المنصة تجاه الراكب
تقدم المنصة خدماتها كما هي، ولا تضمن توفر السائقين في كل الأوقات أو خلو الخدمة من الانقطاعات التقنية المؤقتة.
لا تتحمل المنصة مسؤولية الحوادث المرورية أو الإصابات أو تلف الممتلكات الشخصية أثناء الرحلة، إذ تقع هذه المسؤولية بالكامل على عاتق السائق.
توفر المنصة قنوات تواصل للمساعدة في استعادة المفقودات داخل المركبة دون أن تتحمل مسؤولية قانونية عنها.
لا تتحمل المنصة مسؤولية أي سلوك غير لائق يصدر من السائق، وتتعاون مع السلطات الأمنية العراقية بتزويدهم البيانات اللازمة عند الطلب الرسمي.
لا تتحمل المنصة المسؤولية عن أي تأخير أو انقطاع ناتج عن أسباب خارجة عن إرادتها كانقطاع الإنترنت أو القرارات الحكومية أو الظروف الأمنية.

تاسعاً: الامتثال القانوني
يلتزم الراكب بقانون المرور العراقي في كل ما يتعلق بالسلامة والسلوك داخل المركبة.
يلتزم الراكب بقانون جرائم المعلوماتية العراقي، ويُحظر استخدام التطبيق لإزعاج الآخرين أو اختراق الخصوصية أو أي نشاط يخالف الآداب العامة.
في حال أي نزاع قانوني ينشأ عن استخدام المنصة، يكون القضاء العراقي هو المرجع الوحيد لحل النزاعات.
جميع السجلات الرقمية وبيانات GPS والرسائل المتبادلة عبر التطبيق لها حجية قانونية كاملة أمام المحاكم العراقية.
تعتبر ضغطة الزر داخل التطبيق كتأكيد الطلب بمثابة توقيع إلكتروني ملزم يثبت موافقة الراكب على العملية والالتزامات المالية المترتبة عليها.
يقر الراكب بأن استخدامه لوسائل الاتصال عبر التطبيق يخضع للقوانين والأنظمة الصادرة عن هيئة الإعلام والاتصالات العراقية.

شروط وأحكام الكابتن (السائق)
جميع الحقوق والالتزامات الخاصة بالسائق عند التسجيل والعمل في تطبيق ريدو


هذا المستند يوضح بشكل تفصيلي جميع الحقوق والالتزامات والمسؤوليات الخاصة بالكابتن (السائق) عند التسجيل والعمل في تطبيق ريدو. يرجى قراءة هذه الشروط بعناية قبل بدء العمل، إذ يُعدّ تسجيلك في المنصة وقبولك للرحلات موافقة صريحة على جميع البنود الواردة أدناه.

أولاً: الوضع القانوني للسائق
استقلالية السائق
جميع السائقين المسجلين في التطبيق هم أطراف خارجية مستقلة (Independent Contractors).
السائق ليس موظفاً ولا وكيلاً ولا ممثلاً لتطبيق ريدو بأي شكل من الأشكال.
لا تنشأ بين السائق والمنصة أي علاقة عمل أو توظيف أو شراكة قانونية.
طبيعة العلاقة التعاقدية
دور منصة ريدو ينتهي بمجرد ربط الراكب بالسائق المناسب عبر التطبيق.
عند قبول السائق لطلب الرحلة، ينشأ عقد نقل مباشر بينه وبين الراكب دون أي تدخل من المنصة.
السائق هو الطرف الملزم بنقل الراكب من نقطة الانطلاق إلى نقطة الوصول المحددة مقابل الرسوم المتفق عليها.

ثانياً: التراخيص والوثائق المطلوبة
إجازة السوق
يجب أن يحمل السائق إجازة سوق عراقية سارية المفعول وصالحة وقت التسجيل وطوال فترة العمل.
يجب أن تكون فئة الإجازة متناسبة مع نوع المركبة المستخدمة في تقديم الخدمة.
أوراق المركبة
يجب أن تكون السيارة مسجلة رسمياً لدى مديرية المرور العامة بسنوية نافذة وغير منتهية الصلاحية.
يجب أن تحمل المركبة لوحات تسجيل أصولية ومعتمدة رسمياً.
وثائق الهوية والأمن
يلتزم السائق بتقديم هوية الأحوال المدنية أو البطاقة الوطنية الصالحة لدى التسجيل.
يقر السائق بصحة كافة مستمسكاته الثبوتية المرفوعة للمنصة ومطابقتها للواقع.
يتحمل السائق المسؤولية القانونية الكاملة عن أي تلاعب أو تزوير في هذه البيانات.
يُعتبر تقديم أي وثيقة مزورة أو غير عائدة للشخص نفسه جريمة يحاسب عليها القانون العراقي وتؤدي إلى الحظر الفوري وتقديم البيانات للسلطات الأمنية.

ثالثاً: مسؤوليات السائق الكاملة
المسؤوليات التشغيلية
صيانة المركبة وجهوزيتها الفنية والحفاظ عليها في حالة جيدة وآمنة في جميع الأوقات.
الالتزام الكامل بقواعد المرور والقوانين المحلية للنقل المعمول بها في العراق.
ضمان سلامة الراكب طوال فترة الرحلة من لحظة الانطلاق حتى الوصول.
الالتزام بمعايير الجودة المطلوبة من المنصة والحفاظ على مستوى خدمة عالٍ.
الالتزامات تجاه الراكب
الوصول لنقطة الانطلاق في الوقت المناسب وإخطار الراكب فور الوصول.
التعامل باحترام تام مع الراكب في جميع مراحل الرحلة.
الحفاظ على نظافة المركبة وتوفير بيئة مريحة وآمنة للراكب.
حق رفض الرحلة أو إلغائها
يحق للسائق رفض أو إلغاء الرحلة إذا تبين أن الغرض منها نقل بضائع دون وجود راكب.
يحق للسائق إلغاء الرحلة إذا لاحظ حيازة الراكب لمواد غير قانونية أو مشبوهة.
يحق للسائق رفض الرحلة في حال تصرف الراكب بصورة مسيئة أو غير لائقة.
المسؤولية عن الحوادث
السائق هو المسؤول الوحيد عن أي حوادث مرورية أو إصابات ناتجة عن الرحلة.
تخضع الحوادث المرورية لأحكام التأمين الخاص بالمركبة وللتشريعات العراقية المحلية النافذة.
لا تتحمل منصة ريدو أي مسؤولية عن الحوادث المرورية أو تصرفات السائق خلال الرحلة.

رابعاً: الأنشطة المحظورة على السائق
المحظورات القانونية والأمنية
يُمنع منعاً باتاً نقل أي مواد يجرمها القانون العراقي كالأسلحة والذخائر والمواد المخدرة والمشروبات الكحولية والمواد المسروقة.
يُحظر نقل المواد القابلة للاشتعال أو الكيميائية الخطرة أو أي أشياء تهدد سلامة الركاب أو المركبة.
يُحظر انتحال الشخصية أو استخدام وثائق مزورة أو بيانات وهمية عند التسجيل أو أثناء العمل.
محظورات الخدمة
يُمنع الاتفاق على رحلات خارج نظام التطبيق للتهرب من رسوم المنصة، وفي هذه الحالة لا تتحمل المنصة أي مسؤولية عن أي حادث أو نزاع.
يُحظر القيام بأي تصرفات تعطل عمل المنصة تقنياً أو تضر بسمعتها أو مصالحها.
يُمنع استخدام التطبيق لإجراء طلبات وهمية أو التلاعب بنظام التسعير أو العروض الترويجية.
السلوك والآداب العامة
يُمنع منعاً باتاً أي شكل من أشكال التحرش سواء لفظياً أو فعلياً تجاه الركاب.
يُمنع التمييز بناءً على الدين أو الطائفة أو العرق أو الجنس عند قبول الرحلات أو التعامل مع الركاب.
يُحظر تصوير الراكب أو تسجيل صوته دون إذن صريح منه.
يُمنع الاحتفاظ ببيانات اتصال الراكب أو التواصل معه خارج نطاق المنصة بعد انتهاء الرحلة.
يُمنع الخوض في نقاشات سياسية أو طائفية حادة مع الركاب للحفاظ على سلامة الجميع وراحتهم.
المحظورات التقنية
يُحظر استخدام برامج GPS وهمية للتلاعب بالموقع الجغرافي أو بيانات الرحلة.
يُمنع محاولة اختراق التطبيق أو التلاعب ببياناته أو استخدام أدوات برمجية غير مصرح بها.
يُحظر إنشاء أكثر من حساب للتلاعب بالعروض الترويجية أو رسوم المنصة.

خامساً: التوثيق والمراقبة
كاميرات المراقبة داخل المركبة
يحق للسائق بموافقة المنصة استخدام كاميرات مراقبة داخل المركبة لأغراض التوثيق الأمني، مع مراعاة القوانين العراقية الخاصة بالخصوصية.
في حال وجود كاميرات مراقبة داخل المركبة، يجب وضع ملصق واضح في مكان بارز يُعلم الراكب بوجودها قبل صعوده.
تُستخدم تسجيلات الكاميرات فقط لغرض التوثيق الأمني والقانوني عند الضرورة، وليس لأي أغراض أخرى.
سجلات الرحلات
تقوم المنصة بتوثيق وحفظ سجلات الرحلات كاملة، تشمل المسار والوقت والأجرة وبيانات الأطراف، في قواعد بيانات مؤمنة.
تُعامل هذه البيانات بخصوصية تامة ولا يتم الإفصاح عنها إلا في حالات النزاع القانوني أو بناءً على أمر قضائي أو طلب من الجهات الأمنية المختصة.
تسجيل المكالمات
قد يتم تسجيل أو مراقبة الاتصالات التي تتم عبر التطبيق لضمان جودة الخدمة وسلامة جميع الأطراف.
تُستخدم هذه التسجيلات فقط في حالات التحقيق في الشكاوى أو التدريب أو الامتثال للجهات القانونية العراقية المختصة.

سادساً: الاتصالات والتواصل
التواصل مع الراكب
يوفر التطبيق وسيلة اتصال للتنسيق بشأن نقطة الالتقاء أو تفاصيل الرحلة فقط، ولا يجوز استخدامها لأي غرض آخر.
يجب أن تظل جميع الاتصالات ضمن إطار الاحترام المتبادل والمهنية في جميع الأوقات.
يُمنع استخدام وسائل التواصل للتحرش أو الإساءة اللفظية أو لأغراض شخصية خارج نطاق الرحلة.
بمجرد اكتمال الرحلة تنتهي صلاحية التواصل مع الراكب، ويُحظر مراسلته أو الاتصال به خارج نطاق المنصة لأي سبب كان.
الرسائل الواردة من المنصة
بالتسجيل في المنصة يوافق السائق على تلقي رسائل تشغيلية مثل إشعارات الرحلات وتحديثات الحساب والفواتير.
يتلقى السائق إشعارات إدارية تشمل تحديثات الشروط والأحكام وتنبيهات الأمان وتحديثات التطبيق.
يقر السائق بأن استخدامه لوسائل الاتصال عبر التطبيق يخضع للقوانين والأنظمة الصادرة عن هيئة الإعلام والاتصالات العراقية.

سابعاً: الحقوق المالية للسائق
رسوم الانتظار
تمنح المنصة الراكب فترة سماح مجانية عند وصول السائق لنقطة الانطلاق (مثلاً 5 دقائق).
بعد انتهاء فترة السماح يحق للسائق بدء احتساب رسوم انتظار تضاف إلى القيمة الإجمالية للرحلة.
رسوم الإلغاء
يحق للسائق تلقي رسوم إلغاء في حال قام الراكب بالإلغاء بعد مرور وقت معين من قبوله للطلب.
تفرض رسوم إلغاء أيضاً إذا وصل السائق لنقطة الانطلاق ولم يخرج الراكب في الوقت المحدد.
تعرفة الرحلة واحتساب الأجرة
يتم احتساب تكلفة الرحلة بناءً على معادلة تشمل: أجرة الأساس، المسافة المقطوعة، ووقت الرحلة.
قد تتأثر الأسعار في أوقات الذروة، ويظهر السعر التقديري للراكب في التطبيق قبل تأكيد الطلب.
عند نهاية كل رحلة يتم إصدار ملخص إلكتروني يوضح تفاصيل الأجرة لكلا الطرفين.

ثامناً: حقوق السائق على المنصة
التمكين التقني والتدريب
تلتزم منصة ريدو بتوفير التدريب اللازم والأدوات التقنية للسائقين لمساعدتهم على إدارة دخلهم وتطوير مهاراتهم.
توفر المنصة للسائقين فرص عمل مرنة تساهم في تعزيز مهاراتهم في التعامل مع التكنولوجيا ورفع مستوى خدمتهم.
حق الوصول إلى البيانات
يحق للسائق الاطلاع على بياناته الشخصية المحتفظ بها في المنصة، وتشمل: معلومات الملف الشخصي، سجل الرحلات السابقة، التقييمات والملاحظات، والسجل المالي.
يمكن للسائق في أي وقت تعديل بياناته غير الدقيقة عبر إعدادات التطبيق أو التواصل مع الدعم الفني.
حق طلب حذف الحساب
يحق للسائق طلب إغلاق حسابه وحذف بياناته الشخصية عبر إعدادات التطبيق أو بإرسال طلب رسمي إلى بريد الدعم الفني المعتمد.
مع مراعاة أن بعض البيانات قد تُحتفظ بها للامتثال القانوني أو في حال وجود نزاعات قائمة أو مبالغ مالية غير مسددة.
بعد معالجة طلب الحذف لن يكون السائق قادراً على استعادة سجلات الرحلات أو الأرصدة المرتبطة بالحساب المحذوف.

تاسعاً: الامتثال القانوني
يلتزم السائق بقانون المرور العراقي في كل ما يتعلق بالسلامة والسرعة والالتزام بقواعد السير وحماية الركاب.
يلتزم السائق بقانون جرائم المعلوماتية العراقي، ويُحظر استخدام التطبيق لإزعاج الآخرين أو اختراق الخصوصية أو أي نشاط يخالف الآداب العامة.
في حال أي نزاع قانوني ينشأ عن استخدام المنصة، يكون القضاء العراقي هو المرجع الوحيد لحل النزاعات، والمحاكم في بغداد هي المختصة مكانياً.
جميع السجلات الرقمية وبيانات GPS والرسائل المتبادلة عبر التطبيق لها حجية قانونية كاملة أمام المحاكم العراقية والجهات الرسمية.
تعتبر ضغطة الزر داخل التطبيق كقبول الرحلة أو إنهائها بمثابة توقيع إلكتروني ملزم يثبت موافقة السائق على العملية والالتزامات المترتبة عليها.
لكي يُعتدّ بأي شكوى أو خلاف يجب توثيقها عبر القنوات الرسمية داخل التطبيق في قسم الدعم الفني، ولا يُعتدّ بأي اتفاقات شفهية أو مراسلات خارج نطاق المنصة.

عاشراً: حظر الحساب وإيقافه
يحق للمنصة حظر حساب السائق فوراً في حال تقديم وثائق مزورة أو مخالفة الشروط والأحكام المنصوص عليها.
يتعرض السائق للحظر الفوري عند القيام بأي نشاط مخالف للقوانين العراقية النافذة أو لمعايير المنصة.
في حالات الحظر، تحتفظ المنصة بحق تقديم بيانات السائق للسلطات الأمنية المختصة عند الطلب الرسمي.
يحق للمنصة إيقاف حساب السائقين غير الملتزمين بمعايير الجودة والمهنية المطلوبة بعد تنبيههم.
يحق للمنصة اتخاذ الإجراءات القانونية اللازمة في حال إلحاق السائق ضرراً بالمنصة أو بالركاب أو بالغير.

